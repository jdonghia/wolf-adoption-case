class Wolf {
    constructor(name, img, age, status, description) {
        this.name = name;
        this.img = img;
        this.age = age;
        this.status = status;
        this.description = description;
    }

    getName() {
        return this.name;
    }

    getImg() {
        return this.img;
    }

    getAge() {
        return this.age;
    }
}